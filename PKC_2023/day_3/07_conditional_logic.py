# Logical operators are either True or False, yes or no, or 1 or 0
# equal to                          ==
# not equal to                      !=
# greater than                      >
# less than                         <
# greater than or equal to          >=
# less than or equal to             <=

# is 5 equal to 5?
print(5 == 5)

# is 5 not equal to 5?
print(5 != 5)

# is 6 greater than 5?
print(6 > 5)

# is 6 less than 5?
print(6 < 5)

# is 8 greater than or equal to 7?
print(8 >= 7)

# is 9 less than or equal to 8?
print(9 <= 8)

# application of logical operators
fareha_age = 3
minimum_age_at_school = 4
print(fareha_age == minimum_age_at_school)


# input and logical operators
minimum_age_at_school = 4
fareha_age = input("How old is Fareha? ")
fareha_age = int(fareha_age)
print(fareha_age == minimum_age_at_school)

